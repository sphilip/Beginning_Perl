#!/usr/bin/perl -w

# string comparison
print "Alpha before Bravo? ", "Alpha" < "Bravo", "\n";

# F has smaller ASCII than f, hence Fowl comes before fish
print "Fowl before fish?" , "Fowl" < "fish", "\n";

# use ord() to convert string to ASCII
print "# in ASCII = ", ord("#"), "\n";
print "* in ASCII = ", ord("*"). "\n";


